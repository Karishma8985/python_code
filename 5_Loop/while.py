#While Loop is used to execute a block of statements repeatedly 
# until a given condition is satisfied. When the condition becomes false, 
# the line immediately after the loop in the program is executed.
i = 1
while i < 101:
  print(i,end=" ")
  i += 1
  
# difference between for and while is for can work only when we have both start as well as end point
#but while can work without start or end.. when we work without atart and end it will goes in infinity
