# match is sam e as switch but difference is there is no break
# default and  break is here in match,
# you can match case with number , char as well as string....

ch = int(input("enter your choice..(1-7)"))
match (ch):
    case 1:
        print(f" MONDAY")

    case 2:
        print(f" TUESDAY")

    case 3:
        print(f" WEDNESDAY")

    case 4:
        print(f" THURSDAY")

    case 5:
        print(f" FRIDAY")

    case 6:
        print(f" SATURDAY")

    case 7:
        print(f" SUNDAY")

